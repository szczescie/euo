        register auto const _euo_err_union = _euo_identity(__VA_ARGS__); \
        if (_euo_failed(_euo_err_union))                                 \
            return _euo_err(T, _euo_errcode(_euo_err_union));            \
        _euo_val(_euo_err_union);                                        \
